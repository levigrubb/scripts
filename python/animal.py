class Animal:

   alive = True

   def eat(self):
      print('This animal is eating')

   def sleep(self):
      print('this animal is sleeping')

class Rabbit(Animal):
   def Run(self):
      print('this rabbit is running')

class Fish(Animal):
   def Swim(self):
      print('this fish is swimming')

class Hawk(Animal):
   def Fly(self):
      print('this hawk is flying')

